#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	while (len--)
	{
		*ptr++ = (unsigned char)c;
	}
	return (s);
}
